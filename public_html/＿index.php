<div class="section" id="sec4">
<h2 class="title"><img src="img/top/140416/140416seturitu-32.png" width="735" height="89" alt="会社の種類別特徴"/></h2>
<div class="section-body">
<div class="body">
<div class="entry">
<p style="text-align:center; padding:20px 0;"><img src="img/top/140416/140416seturitu-20.png" width="561" height="190" alt="株式会社（物的会社）持分会社（人的会社）"/></p>
<p>「会社」の種類は、１．株式会社、２．合同会社、３．合名会社、４．合資会社の以上４つがあります。これらの４つは、株式会社（物的会社）と持分会社（人的会社）である合同会社、合名会社、合資会社の２つに分類されます。</p>
</div>
<div class="box">
<div class="box-left">
<p><img src="img/top/140416/140416seturitu-21.png" width="331" height="101" alt="株式会社"/></p>
<p>株式会社は所有と経営が分離され、会社の実質的な所有者である株主は経営に関与せず、業務の執行は、 会社経営の専門家である取締役等が執行します。株主にも株主総会等を通じて経営に関与可能な共益権がありますが、一般的に株主は、会社経営より、配当金請求等（自益権）や株の転売利益等を目的とするに重きを置きます。</p>
<p style="margin:15px 0 8px 0;"><img src="img/top/140416/140416seturitu-23.png" width="317" height="57" alt="責任"/></p>
<p>株主は、経営に関与しないのが原則で、出資した範囲で責任を負います（有限責任）。例えば、会社が負債を抱えて倒産した場合、株券はただの紙切れになりますが、会社債権者に対する責任は負いません。</p>
<p style="text-align:center; "><img src="img/top/140416/140416seturitu-24.png" width="317" height="191" alt=""/></p>
</div>

<div class="box-right">
<p><img src="img/top/140416/140416seturitu-22.png" width="331" height="101" alt="持分会社"/></p>
<p>持分会社は、原則として出資者（社員）の総意に基づいた会社経営がなされ、業務執行は、出資者である社員全員で行う、所有と経営が一体化した会社形態です。なお、合同会社の社員は全員が有限責任社員となるため、株式会社に準じた規定が適用されます。</p>
<p style="margin:15px 0 8px 0;"><img src="img/top/140416/140416seturitu-23.png" width="317" height="57" alt="責任"/></p>
<p>合同会社は、出資者全員が有限責任社員であり、株式会社のような機関設計が不要な会社形態です。会社の経営は、原則として社員全員の総意に基づき執行されます。会社の設立費用が株式会社と比べ安く、手続きも簡単、しかも、社員は経営に関与しても出資の範囲で責任を負う有限責任なので、なるべく簡単に早く会社を設立したい方にとって人気のある会社形態です。合名会社は、会社債権者にも社員が連帯責任を負う無限責任社員のみで構成する会社です。原則として、社員全員が代表権を持ちます。家族等のごく限られた少人数の会社に適する形態です。</p>
